// Return the glyph rows for a character, mapping lowercase→uppercase and
// falling back to a blank cell for anything unsupported.
export function glyphFor(ch: string): string[] {
  if (GLYPHS[ch]) return GLYPHS[ch];
  const up = ch.toUpperCase();
  if (GLYPHS[up]) return GLYPHS[up];
  return GLYPHS[' '];
}
